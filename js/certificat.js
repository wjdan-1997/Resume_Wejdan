

function certificat1(){
    var img = document.getElementById('Certificate1');
    var butt = document.getElementById('button1')
  if (img.style.display == "none")
  {
    img.style.display = "block";
    butt.innerHTML = "Hide";
  }else{
    img.style.display ="none";
    butt.innerHTML = "Show";
  }

}

